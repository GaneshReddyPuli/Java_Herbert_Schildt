public class ModDemo {
    public static void main(String[] args){
        int iresult,irem;
        double dresult,drem;
        iresult=10/3;
        irem=10%3;
        dresult=5.0/2.5;
        drem=5.0%2.5;
        System.out.println("Result and reminder of 10/3 are "+iresult+" "+irem);
        System.out.println("Result and reminder of 10.0/3.0 are "+dresult+" "+drem);
    }
    
}
